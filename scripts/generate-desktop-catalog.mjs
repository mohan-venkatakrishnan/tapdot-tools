// Regenerates electron/src/renderer/tools-catalog.js from shared/shared.js's
// TOOL_REGISTRY — the same source of truth the web site and search palette use.
// Run this after adding/removing/renaming any tool: node scripts/generate-desktop-catalog.mjs

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');

const src = fs.readFileSync(path.join(root, 'shared/shared.js'), 'utf8');

const registryMatch = src.match(/const TOOL_REGISTRY = \[([\s\S]*?)\n\];/);
if (!registryMatch) throw new Error('Could not find TOOL_REGISTRY in shared.js');
const registryBody = registryMatch[1];

const labelsMatch = src.match(/const COLLECTION_LABELS = \{([\s\S]*?)\n\};/);
if (!labelsMatch) throw new Error('Could not find COLLECTION_LABELS in shared.js');
const labelsBody = labelsMatch[1];

const entryRe = /\{\s*name:\s*'((?:[^'\\]|\\.)*)',\s*url:\s*'([^']+)',\s*collection:\s*'([^']+)',\s*desc:\s*(?:'((?:[^'\\]|\\.)*)'|"((?:[^"\\]|\\.)*)")/g;
let m;
const all = [];
while ((m = entryRe.exec(registryBody))) {
  all.push({ name: m[1], url: m[2], collection: m[3], desc: m[4] !== undefined ? m[4] : m[5] });
}

// Skip hub/utility pages — the desktop sidebar lists individual tools only.
const SKIP_URLS = new Set([
  '/', '/study/', '/write/', '/dev/', '/marketing/', '/finance/', '/legal/',
  '/hr/', '/health/', '/design/', '/productivity/', '/ai/',
  '/browse/', '/graph/', '/privacy.html',
]);
const tools = all.filter((t) => !SKIP_URLS.has(t.url));

const labelRe = /(\w+):\s*'([^']+)'/g;
const labels = {};
let lm;
while ((lm = labelRe.exec(labelsBody))) labels[lm[1]] = lm[2];

const COLLECTION_ORDER = ['study', 'write', 'dev', 'marketing', 'finance', 'legal', 'hr', 'health', 'design', 'productivity', 'ai'];

const byCollection = {};
tools.forEach((t) => (byCollection[t.collection] ||= []).push(t));

const collections = COLLECTION_ORDER.filter((c) => byCollection[c]).map((c) => ({
  id: c,
  label: labels[c] || c,
  tools: byCollection[c].map((t) => {
    // '/dev/json/' -> { toolId: 'json', htmlPath: 'dev/json/index.html' }
    const parts = t.url.split('/').filter(Boolean); // ['dev', 'json']
    return {
      id: parts[parts.length - 1],
      label: t.name,
      desc: t.desc,
      htmlPath: parts.join('/') + '/index.html',
    };
  }),
}));

const totalTools = tools.length;

const out = `// AUTO-GENERATED by scripts/generate-desktop-catalog.mjs — do not hand-edit.
// Source of truth: shared/shared.js TOOL_REGISTRY. Regenerate after any tool
// add/remove/rename: node scripts/generate-desktop-catalog.mjs
window.TOOLS_CATALOG = {
  totalTools: ${totalTools},
  collections: ${JSON.stringify(collections, null, 2)},
};
`;

const outPath = path.join(root, 'electron/src/renderer/tools-catalog.js');
fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, out);
console.log(`Wrote ${outPath} — ${totalTools} tools across ${collections.length} collections.`);

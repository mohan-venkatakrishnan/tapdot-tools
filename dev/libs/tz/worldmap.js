const TZMAP_W = TZMAP_COLS * 10, TZMAP_H = TZMAP_ROWS * 10;
const tzmX = (lon) => (lon + 180) / 360 * TZMAP_W;
const tzmY = (lat) => (90 - lat) / 180 * TZMAP_H;

function tzmDots() {
  let out = '';
  for (let r = 0; r < TZMAP_ROWS; r++) {
    const row = TZMAP_LAND[r];
    for (let c = 0; c < TZMAP_COLS; c++) {
      if (row[c] === '1') out += `<circle class="tzm-land" cx="${c * 10 + 5}" cy="${r * 10 + 5}" r="2.1"/>`;
    }
  }
  return out;
}

// Night hemisphere as 1-2 rects (handles the antimeridian wrap), soft blurred edges.
function tzmNightOverlay(date) {
  const decimalUTC = date.getUTCHours() + date.getUTCMinutes() / 60;
  const subsolarLon = -(decimalUTC - 12) * 15;
  let center = subsolarLon + 180;
  center = ((center + 180) % 360 + 360) % 360 - 180; // wrap to -180..180
  const startLon = center - 90, endLon = center + 90;
  const x1 = tzmX(startLon), x2 = tzmX(endLon);
  let rects;
  if (x1 <= x2) {
    rects = `<rect class="tzm-night" x="${x1}" y="0" width="${x2 - x1}" height="${TZMAP_H}"/>`;
  } else {
    rects = `<rect class="tzm-night" x="${x1}" y="0" width="${TZMAP_W - x1}" height="${TZMAP_H}"/>` +
            `<rect class="tzm-night" x="0" y="0" width="${x2}" height="${TZMAP_H}"/>`;
  }
  return `<g style="filter:url(#tzmBlur)" opacity="0.42">${rects}</g>`;
}

function tzmArcPath(a, b) {
  const x1 = tzmX(a.lon), y1 = tzmY(a.lat), x2 = tzmX(b.lon), y2 = tzmY(b.lat);
  const mx = (x1 + x2) / 2, my = Math.min(y1, y2) - Math.abs(x2 - x1) * 0.12 - 12;
  return `M${x1},${y1} Q${mx},${my} ${x2},${y2}`;
}

/**
 * renderWorldMap(container, opts)
 * opts.cities: TZ_CITIES-shaped array [name, tz, lat, lon]
 * opts.selected: array of tz strings currently "added"
 * opts.date: Date used for the day/night terminator (defaults to now)
 * opts.onToggle: (tz) => void — called when a marker is clicked
 */
function renderWorldMap(container, opts) {
  const { cities, selected = [], date = new Date(), onToggle } = opts;
  const selSet = new Set(selected);
  const selCoords = selected.map(tz => {
    const c = cities.find(c => c[1] === tz);
    return c ? { lat: c[2], lon: c[3] } : null;
  }).filter(Boolean);

  let arcs = '';
  for (let i = 0; i < selCoords.length - 1; i++) {
    arcs += `<path class="tzm-arc" style="animation-delay:${i * 0.15}s" d="${tzmArcPath(selCoords[i], selCoords[i + 1])}"/>`;
  }

  let markers = '';
  cities.forEach(([name, tz, lat, lon], i) => {
    const isSel = selSet.has(tz);
    const x = tzmX(lon), y = tzmY(lat);
    markers += `<g class="tzm-marker${isSel ? ' on' : ''}" data-tz="${tz}" transform="translate(${x},${y})" style="animation-delay:${(i % 12) * 0.18}s">
      <circle class="tzm-ring"/><circle class="tzm-dot"/>
      <title>${name}</title>
    </g>`;
  });

  container.innerHTML = `
    <svg class="tzm-svg" viewBox="0 0 ${TZMAP_W} ${TZMAP_H}" preserveAspectRatio="xMidYMid meet" role="img" aria-label="World map">
      <defs><filter id="tzmBlur" x="-20%" y="-20%" width="140%" height="140%"><feGaussianBlur stdDeviation="14"/></filter></defs>
      <rect class="tzm-sea" x="0" y="0" width="${TZMAP_W}" height="${TZMAP_H}"/>
      <g class="tzm-dots">${tzmDots()}</g>
      ${tzmNightOverlay(date)}
      <rect class="tzm-sweep" x="-60" y="0" width="60" height="${TZMAP_H}"/>
      <g class="tzm-arcs">${arcs}</g>
      <g class="tzm-markers">${markers}</g>
    </svg>`;

  if (onToggle) {
    container.querySelectorAll('.tzm-marker').forEach(el => {
      el.addEventListener('click', () => onToggle(el.dataset.tz));
    });
  }
}
